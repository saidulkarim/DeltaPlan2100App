package com.cegis.deltaplan2100.models;

public class ListViewItems {
    int itemID;
    String itemName;
    String itemIcon;
    String itemParentID;

    public int getItemID() {
        return itemID;
    }

    public void setItemID(int Name) {
        itemID = Name;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String Name) {
        itemName = Name;
    }

    public String getItemIcon() {
        return itemIcon;
    }

    public void setItemIcon(String icon) {
        itemIcon = icon;
    }

    public String getItemParentID() {
        return itemParentID;
    }

    public void setItemParentID(String parentID) {
        itemParentID = parentID;
    }

}
