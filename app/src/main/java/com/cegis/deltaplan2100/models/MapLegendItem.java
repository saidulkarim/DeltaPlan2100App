package com.cegis.deltaplan2100.models;

public class MapLegendItem {
    private String title;
    private String subTitle;
    private String sourceLayerName;
    private String fillLayerName;
    private String fillLayerColor;
    private String lineLayerName;
    private String lineStyle;
    private String lineLayerColor;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSubTitle() {
        return subTitle;
    }

    public void setSubTitle(String subTitle) {
        this.subTitle = subTitle;
    }

    public String getSourceLayerName() {
        return sourceLayerName;
    }

    public void setSourceLayerName(String sourceLayerName) {
        this.sourceLayerName = sourceLayerName;
    }

    public String getFillLayerName() {
        return fillLayerName;
    }

    public void setFillLayerName(String fillLayerName) {
        this.fillLayerName = fillLayerName;
    }

    public String getFillLayerColor() {
        return fillLayerColor;
    }

    public void setFillLayerColor(String fillLayerColor) {
        this.fillLayerColor = fillLayerColor;
    }

    public String getLineLayerName() {
        return lineLayerName;
    }

    public void setLineLayerName(String lineLayerName) {
        this.lineLayerName = lineLayerName;
    }

    public String getLineStyle() {
        return lineStyle;
    }

    public void setLineStyle(String lineStyle) {
        this.lineStyle = lineStyle;
    }

    public String getLineLayerColor() {
        return lineLayerColor;
    }

    public void setLineLayerColor(String lineLayerColor) {
        this.lineLayerColor = lineLayerColor;
    }
}
